// Kotlin Program to Multiply two Floating Point Numbers and Program to Find ASCII value of a character

fun main(){
    var num1:Float = 3f
    var num2:Float = 5f

    println("Multiplication of $num1 and $num2 is: ${num1 * num2}")

    var char:Char = 'a'

    println("The ASCII value of '$char' is: ${char.code}")
}
